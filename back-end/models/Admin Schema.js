const mongoose = require('mongoose');

const Notification = require('./User_Notification Schema');

const adminSchema = new mongoose.Schema({

    name: {
        type: String,
        required: true,
    },
      username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true,
    },
    
    profilePicture: {
        type: Buffer,
        validate: {
          validator: function (v) {   
            return /\.(png|jpg|jpeg)$/.test(v);
          },
          message: props => `${props.value} is not a valid image file. Please use PNG, JPG, or JPEG.`,
        },
      },

  notifications: [{ type: mongoose.Schema.Types.ObjectId, ref: 'admin_Notification' }],
});

const Admin =   mongoose.model('Admin', adminSchema);

module.exports = Admin;
